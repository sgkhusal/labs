#include "encoder.h"

/* Encoder:
	open file
	read file
	apply huffman coding
	6. Send the encoded text to the shared memory
	7. Read the info from the decoder and display it
	close file
	*/

/* Decoder:
	open file
	read file
	1. Aplly the translation table */

int	encoder(int nstrs, unsigned char **strs)
{
	unsigned char	*initial_str;
	unsigned int	*freq;
	t_huffman		*head;
	char			**d;
	char			*str_bit;
	size_t			nbits;
	unsigned char	*bits;

	// verificar se a string recebida Ã© nula -> transformar em ""
	// juntar as strings
	printf("********** Initial string **********\n");
	initial_str = strs[1];
	printf("%s\n\n", initial_str);

	freq = huffman_frequency(initial_str);
	head = create_huffman_lst(freq);
	head = create_huffman_tree(&head);
	d = dictionary(head);

	printf("********** String of bits from encode **********\n");
	str_bit = encode(d, initial_str);
	printf("%s\n\n", str_bit);

	printf("********** String generated by compression **********\n");
	nbits = number_of_bits(str_bit);
	bits = compress(str_bit, nbits);
	printf("%s\n\n", bits);

	(void)nstrs;
	return (0);
}
